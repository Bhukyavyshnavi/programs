#include<bits/stdc++.h>
using namespace std;
int main(){
	string s;
	int n;
	cin>>s>>n;
	if(s=="march" && n ==20 ){
		cout<<"Spring"<<"\n";
	}
	else if(s=="june" && n==21){
		cout<<"summer"<<"\n";
	}
	else if(s=="september" && n==22){
		cout<<"fall"<<"\n";
	}
	else if(s=="december" && n==21){
		cout<<"winter"<<"\n";
	}
	
	
}
